async function seedTradePrograms() {
  console.log('ðŸŒ± Starting trade programs seed...');
  
  for (const program of tradePrograms) {
    try {
      await prisma.tradeProgram.create({
        data: {
          name: program.name,
          trade: program.trade,
          description: program.description,
          provider: program.provider,
          providerType: program.providerType,
          city: program.city,
          state: program.state,
          isOnline: program.isOnline,
          duration: program.duration,
          tuition: program.tuition,
          jobPlacementRate: program.jobPlacementRate,
          averageStartingSalary: program.averageStartingSalary,
          medianSalary: program.medianSalary,
          certificationsEarned: program.certificationsEarned,
          disabilityServicesAvailable: program.disabilityServicesAvailable,
          accommodationsOffered: program.accommodationsOffered,
          prerequisites: program.prerequisites,
          accreditationBody: program.accreditationBody,
          financialAidAvailable: program.financialAidAvailable,
          isActive: true,
        },
      });
      console.log(`  âœ“ Created: ${program.name}`);
    } catch (error) {
      console.error(`  âœ— Failed: ${program.name}`, error);
    }
  }
  
  console.log(`\nâœ… Seeded ${tradePrograms.length} trade programs`);
  
  // Print summary by trade
  const summary: Record<string, number> = {};
  for (const program of tradePrograms) {
    summary[program.trade] = (summary[program.trade] || 0) + 1;
  }
  
  console.log('\nðŸ“Š Programs by trade:');
  for (const [trade, count] of Object.entries(summary)) {
    console.log(`  ${trade}: ${count} programs`);
  }
}

// Run the seed
seedTradePrograms()
  .then(async () => {
    await prisma.$disconnect();
  })
  .catch(async (e) => {
    console.error(e);
    await prisma.$disconnect();
    process.exit(1);
  });

